function startFunction(){
    el = document.getElementById("start-btn")
    if(el.innerHTML == "STOP") 
    {
        el.innerHTML = "START" 
    } else {
        navigator.geolocation.getCurrentPosition(onSuccess, onError);
        el.innerHTML = "STOP"
    }
}
